# @njit
def gravitational_field_parallelepiped(
    xp,
    yp,
    zp,
    xc,
    yc,
    zc,
    lxs,
    lys,
    lzs,
    density,
    G,
):
    """
    Field generated by source parallelepiped.
    """

    x1 = xc - lxs / 2.0
    x2 = xc + lxs / 2.0

    y1 = yc - lys / 2.0
    y2 = yc + lys / 2.0

    z1 = zc - lzs / 2.0
    z2 = zc + lzs / 2.0

    xs = np.array([x1 - xp, x2 - xp])
    ys = np.array([y1 - yp, y2 - yp])
    zs = np.array([z1 - zp, z2 - zp])

    gx = 0.0
    gy = 0.0
    eps = 0

    for i in range(2):
        x = xs[i]

        for j in range(2):
            y = ys[j]

            for k in range(2):
                z = zs[k]

                # s = (-1) ** (i + j + k)
                s = (-1) ** (i + j + k + 1)  # Va el +1?

                r = np.sqrt(x * x + y * y + z * z) + eps

                gx += s * (
                    y * np.log(r + z + eps)
                    + z * np.log(r + y + eps)
                    - x * np.arctan2(y * z, x * r)
                    # - x * np.arctan(y * z / x * r)
                )

                gy += s * (
                    z * np.log(r + x + eps)
                    + x * np.log(r + z + eps)
                    - y * np.arctan2(x * z, y * r)
                    # - y * np.arctan(x * z / y * r)
                )

    gx *= G * density
    gy *= G * density

    return np.array([gx, gy])


