def gravitational_torque_between_parallelepipeds(
    # Observation/target body
    xt,
    yt,
    zt,
    lxt,
    lyt,
    lzt,
    density_target,
    # Orientation of target body
    phi,
    theta,
    psi,
    # Source body
    xs,
    ys,
    zs,
    lxs,
    lys,
    lzs,
    density_source,
    # Gravity
    G,
    nx,
    ny,
    nz,
    # Tolerances for scipy integration (optional but recommended)
    epsabs=1.49e-08,
    epsrel=1.49e-08,
):
    """Computes gravitational torque around z axis on a target parallelepiped

    due to a source parallelepiped using scipy.integrate.tplquad.

    Euler convention:
        z(phi) -> new x(theta) -> new z(psi)

    Returns
    -------
    Torque around z axis
    """

    # Rotation matrix body -> world
    R = euler_zxz(phi, theta, psi)

    # Define the integrand function for tplquad
    # Note: tplquad passes arguments in the order (z, y, x)
    def integrand(zl, yl, xl):
        # Position relative to target COM in body frame
        r_body = np.array([xl, yl, zl])

        # Transform to world frame
        r_world = R @ r_body

        # Absolute world position
        pos_x = xt + r_world[0]
        pos_y = yt + r_world[1]
        pos_z = zt + r_world[2]

        # Gravitational field at this point (per unit mass)
        g = gravitational_field_parallelepiped(
            pos_x,
            pos_y,
            pos_z,
            xs,
            ys,
            zs,
            lxs,
            lys,
            lzs,
            density_source,
            G,
        )

        # Force per unit volume: density * g
        dF_x = density_target * g[0]
        dF_y = density_target * g[1]

        # Torque per unit volume relative to target COM around world Z-axis
        dTau = r_world[0] * dF_y - r_world[1] * dF_x
        return dTau

    # Integration limits for the target body (centered around local origin)
    x_min, x_max = -lxt / 2, lxt / 2
    y_min, y_max = -lyt / 2, lyt / 2
    z_min, z_max = -lzt / 2, lzt / 2

    # tplquad takes limits functions for y and z, but since it's a
    # parallelepiped, the boundaries are constant scalars.
    torque, error = tplquad(
        integrand,
        x_min,
        x_max,
        lambda x: y_min,
        lambda x: y_max,
        lambda x, y: z_min,
        lambda x, y: z_max,
        epsabs=epsabs,
        epsrel=epsrel,
    )

    return torque


# @njit
def moment_of_inertia_two_parallelepipeds_z(
    density,
    lx,
    ly,
    lz,
    r,
):
    """
    Moment of inertia around the global z-axis for
    two identical rectangular parallelepipeds.

    Centers of mass:
        (+r, 0, 0)
        (-r, 0, 0)

    Parameters
    ----------
    density : float
        Density of parallelepipeds.

    lx, ly, lz : float
        Side lengths.

    r : float
        Distance of each center from origin along x.

    Returns
    -------
    Iz : float
        Total moment of inertia around z-axis.
    """
    mass = density * lx * ly * lz
    # Intrinsic inertia of one prism around its own COM
    Iz_cm = (mass / 12.0) * (lx**2 + ly**2)

    # Parallel axis theorem
    Iz_one = Iz_cm + mass * r**2

    # Two identical prisms
    Iz_total = 2.0 * Iz_one

    return Iz_total


# @njit
def d2t_phi(
    phi,
    phi_dot,
    # Densitiy,
    density,
    # Observation/target body
    r,
    lxt,
    lyt,
    lzt,
    # Source body
    xs,
    ys,
    zs,
    lxs,
    lys,
    lzs,
    # Numerical integration resolution
    nx,
    ny,
    nz,
    # Gravity
    G,
    # Pendulum parameters
    k,
    gamma,
    # Moment of inertia
    I,
):
    gravity_torque = (
        gravitational_torque_between_parallelepipeds(  # Observation/target body
            r * np.cos(phi * np.pi / 180),
            r * np.sin(phi * np.pi / 180),
            0,
            lxt,
            lyt,
            lzt,
            density,
            # Orientation of target body
            phi,
            0,
            0,
            # Source body
            xs,
            ys,
            zs,
            lxs,
            lys,
            lzs,
            density,
            # Gravity
            G,
            # Numerical integration resolution
            nx,
            ny,
            nz,
        )
    )
    return (-k * phi - gamma * phi_dot + gravity_torque) / I


